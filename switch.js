const numero = 10

// O switch espera um valor para ser comparado
switch(numero) {
    // O case compara se o valor da variavel numero é 
    //iagual a 2
    case 2: console.log('valor')
    // O break é executado quando a condição for verdadeira
    // ele para o processo de verificação
    break;

    case 5: console.log('valor')
    break

    default: console.log('Valor padrão')
}
